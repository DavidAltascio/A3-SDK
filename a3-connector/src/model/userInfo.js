export class A3UserInfo {
    get userName(){return this._userName;}
    set userName(value){this._userName = value;}
    get userCode(){return this._userCode;}
    set userCode(value){this._userCode = value;}
    get displayName(){return this._displayName;}
    set displayName(value){this._displayName = value;}
    get organizationCode(){return this._organizationCode;}
    set organizationCode(value){this._organizationCode = value;}
    get userIdentityKey(){return this._userIdentityKey;}
    set userIdentityKey(value){this._userIdentityKey = value;}
    get accessToken(){return this._accessToken;}
    set accessToken(value){this._accessToken = value;}
}